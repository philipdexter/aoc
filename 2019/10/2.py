import math
def p(x, y):
  rx = math.cos(-90 * math.pi/180) * x - math.sin(-90 * math.pi/180) * y
  ry = math.sin(-90 * math.pi/180) * x + math.cos(-90 * math.pi/180) * y
  ret = math.degrees(math.atan2(ry, rx))
  if ret < 0:
    ret += 360
  return ret

